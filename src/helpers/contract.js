const chalk = require('chalk')

const Web3Instance = async (config) => {
    const rpc = Random(config.rpcURL)
    console.log(chalk.cyan(`Call:::RPC Provider: ${rpc}`))

    const _web3 = await new Web3(rpc)
    _web3.eth.accounts.wallet.clear()
    _web3.eth.accounts.wallet.add(config.secretPhrase, config.userAddress)
    _web3.eth.defaultAccount = config.userAddress

    return _web3
}

const getBlockNumber = async (config) => {
    const _web3 = await Web3Instance(config)
    return await _web3.eth.getBlockNumber()
}

